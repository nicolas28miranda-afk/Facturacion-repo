-- TAREAS_OPERACION: bandeja operación (tareas, mensajes, notificaciones).
--
-- IMPORTANTE (evita ORA-00942 en el backend):
-- Ejecutar este script conectado como el MISMO usuario Oracle que usa la app
-- (spring.datasource.username en application.yml, p. ej. NICK).
-- Si la tabla se crea en otro esquema, el usuario de la app no la ve.
--
-- Requiere UNIQUE o PK en USUARIOS(NO_USUARIO) para que las FK no fallen.

CREATE TABLE TAREAS_OPERACION (
    ID_TAREA_OPERACION   NUMBER GENERATED BY DEFAULT AS IDENTITY
                         MINVALUE 1 MAXVALUE 9999999999999999999999999999
                         START WITH 1 INCREMENT BY 1
                         CACHE 20 NOORDER NOCYCLE
                         NOT NULL,
    TIPO                 VARCHAR2(20 BYTE) NOT NULL,
    ASUNTO               VARCHAR2(500 BYTE) NOT NULL,
    CUERPO               CLOB,
    NO_USUARIO_DE        VARCHAR2(50 BYTE) NOT NULL,
    NO_USUARIO_PARA      VARCHAR2(50 BYTE) NOT NULL,
    ESTADO               VARCHAR2(20 BYTE) DEFAULT 'PENDIENTE' NOT NULL,
    FECHA_ALTA           DATE DEFAULT SYSDATE NOT NULL,
    FECHA_LEIDO          DATE,
    FECHA_COMPLETADO     DATE,
    FECHA_VENCIMIENTO    DATE,
    URL_ACCION           VARCHAR2(400 BYTE),
    ID_REFERENCIA        VARCHAR2(64 BYTE),
    ID_PERFIL_DE_SNAPSHOT NUMBER(10,0),
    CONSTRAINT PK_TAREAS_OPERACION PRIMARY KEY (ID_TAREA_OPERACION),
    CONSTRAINT CHK_TAREAS_OP_TIPO CHECK (TIPO IN ('TAREA', 'MENSAJE', 'NOTIFICACION')),
    CONSTRAINT CHK_TAREAS_OP_EST CHECK (
        ESTADO IN ('PENDIENTE', 'LEIDO', 'COMPLETADO', 'ARCHIVADO', 'CANCELADO')
    ),
    CONSTRAINT FK_TAREAS_OP_DE FOREIGN KEY (NO_USUARIO_DE)
        REFERENCES USUARIOS (NO_USUARIO),
    CONSTRAINT FK_TAREAS_OP_PARA FOREIGN KEY (NO_USUARIO_PARA)
        REFERENCES USUARIOS (NO_USUARIO)
);

CREATE INDEX IX_TAREAS_OP_PARA_EST_FECHA
    ON TAREAS_OPERACION (NO_USUARIO_PARA, ESTADO, FECHA_ALTA DESC);

CREATE INDEX IX_TAREAS_OP_DE_FECHA
    ON TAREAS_OPERACION (NO_USUARIO_DE, FECHA_ALTA DESC);

COMMENT ON TABLE TAREAS_OPERACION IS
    'Bandeja operación: tareas, mensajes y notificaciones entre usuarios (p. ej. admin → operativo).';
COMMENT ON COLUMN TAREAS_OPERACION.TIPO IS 'TAREA | MENSAJE | NOTIFICACION';
COMMENT ON COLUMN TAREAS_OPERACION.ID_PERFIL_DE_SNAPSHOT IS
    'Opcional: ID_PERFIL del remitente al crear el registro (auditoría).';
